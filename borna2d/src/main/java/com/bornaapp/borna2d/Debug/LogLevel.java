package com.bornaapp.borna2d.Debug;

/**
 * Created by s. Mehdi HashemiNia on 1/3/2017.<p>
 * NONE: mutes all logging.<p>
 * ERROR: logs only error messages.<p>
 * INFO: logs error & info messages(non debug messages).<p>
 * DEBUG: logs all messages.<p>
 */

public enum LogLevel {
    NONE,
    ERROR,
    INFO,
    DEBUG
}
